// convert the following numbers to standard decimal form
// 6.34e5 , 1.95162e2, 3426.95, 4893.2, .321, .123, .006789
#include <stdio.h>
int main(void){
   double a = 6.34e5;
   double b = 1.95162e2;
   double c = 3426.95;
   double d = 4893.2;
   double e = .321;
   double f = .123;
   double g = .006789;
   
   printf("a = %f\n", a);
   printf("b = %f\n", b);
   printf("c = %f\n", c);
   printf("d = %f\n", d);
   printf("e = %f\n", e);
   printf("f = %f\n", f);
   printf("g = %f\n", g);
};