// write the following numbers into exponent notation
// a = 634000.000000
// b = 195.162000
// c = 3426.950000
// d = 4893.200000
// e = 0.321000
// f = 0.123000
// g = 0.006789
#include <stdio.h>
int main(void){
   double a = 634000.000000;
   double b = 195.162000;
   double c = 3426.950000;
   double d = 4893.200000;
   double e = 0.321000;
   double f = 0.123000;
   double g = 0.006789;
   
   printf("a = %e\n", a);
   printf("b = %e\n", b);
   printf("c = %e\n", c);
   printf("d = %e\n", d);
   printf("e = %e\n", e);
   printf("f = %e\n", f);
   printf("g = %e\n", g);
}